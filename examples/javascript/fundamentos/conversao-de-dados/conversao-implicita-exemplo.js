let result1 = "5" + 1;   // Saída: "51" (número convertido para string)
let result2 = "5" * 2;   // Saída: 10 (string convertida para número)
let result3 = "5" == 5;  // Saída: true (coerção de tipo para comparação)

let result4 = "5" === 5; // Saída: false (comparação estrita, sem coerção)

console.log(result1); // "51"
console.log(result2); // 10
console.log(result3); // true
console.log(result4); // false
